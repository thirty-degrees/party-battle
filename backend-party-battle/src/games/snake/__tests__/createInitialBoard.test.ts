function convertBoard2DToCells(board2D: (CellKind | [CellKind, string])[][]): Cell[] {
  const cells: Cell[] = []
  board2D.forEach((row) => {
    row.forEach((cell) => {
      if (Array.isArray(cell)) {
        const [kind, player] = cell
        cells.push({ kind, player })
      } else {
        cells.push({ kind: cell })
      }
    })
  })
  return cells
}
